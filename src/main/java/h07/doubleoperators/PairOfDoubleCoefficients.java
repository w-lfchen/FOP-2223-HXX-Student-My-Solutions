package h07.doubleoperators;

/**
 * Class encapsulating two double values as coefficients.
 */
public class PairOfDoubleCoefficients {

    /**
     * First coefficient.
     */
    public double coeff1;

    /**
     * Second coefficient.
     */
    public double coeff2;

    /**
     * Constructor initializing the two coefficients.
     *
     * @param coeff1    The first coefficient.
     * @param coeff2    The second coefficient.
     */
    public PairOfDoubleCoefficients(double coeff1, double coeff2) {
        this.coeff1 = coeff1;
        this.coeff2 = coeff2;
    }
}
