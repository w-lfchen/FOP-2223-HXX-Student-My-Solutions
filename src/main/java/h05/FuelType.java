package h05;

/**
 * one might use different types of fuel, suche as these three
 */
public enum FuelType {
    /**
     * Gasoline or petrol is a transparent, petroleum-derived flammable liquid that is used primarily as a fuel in most spark-ignited internal combustion engines. It consists mostly of organic compounds obtained by the fractional distillation of petroleum, enhanced with a variety of additives.
     * source: https://en.wikipedia.org/wiki/Gasoline
     */
    GASOLINE,
    /**
     * Diesel fuel, also called diesel oil, is any liquid fuel specifically designed for use in a diesel engine, a type of internal combustion engine in which fuel ignition takes place without a spark as a result of compression of the inlet air and then injection of fuel. Therefore, diesel fuel needs good compression ignition characteristics. The most common type of diesel fuel is a specific fractional distillate of petroleum fuel oil, but alternatives that are not derived from petroleum, such as biodiesel, biomass to liquid (BTL) or gas to liquid (GTL) diesel are increasingly being developed and adopted. To distinguish these types, petroleum-derived diesel is sometimes called petrodiesel in some academic circles.
     * source: https://en.wikipedia.org/wiki/Diesel_fuel
     */
    DIESEL,
    /**
     * Liquefied petroleum gas is a fuel gas which contains a flammable mixture of hydrocarbon gases, specifically propane, propylene, butylene, isobutane and n-butane.
     * source: https://en.wikipedia.org/wiki/Liquefied_petroleum_gas
     */
    LPG,
}
